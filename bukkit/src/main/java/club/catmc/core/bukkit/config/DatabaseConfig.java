package club.catmc.core.bukkit.config;

/**
 * Database configuration settings
 */
public class DatabaseConfig {

    private String host;
    private int port;
    private String database;
    private String username;
    private String password;

    public DatabaseConfig(String host, int port, String database, String username, String password) {
        this.host = host;
        this.port = port;
        this.database = database;
        this.username = username;
        this.password = password;
    }

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public String getDatabase() {
        return database;
    }

    public void setDatabase(String database) {
        this.database = database;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    /**
     * Creates a DatabaseConfig with default values
     */
    public static DatabaseConfig createDefault() {
        return new DatabaseConfig("localhost", 3306, "minecraft", "root", "");
    }
}
